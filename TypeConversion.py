#Type Conversion
a = int("2") #Typecasted by the given command
b = 4.25
sum = a + b #Type conversion of a(int) is done in this step
print(sum)
print(type(a))

#The conversion of one datatype into the other datatype is known as type casting in python or type conversion in python

#Methods for conversion = int(), float(), str(), ord(), hex(), oct(), tuple(), set(), list(), dict()

#Explicit Conversion - Done by the programmer
#Implicit Conversion - Done by the python itself, this is done according to the level of ordering of datatypes in python
